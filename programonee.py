#This program will convert a temperature in Centigrade to a temperature in Fahrenheit

#Input
#This asks for user input on the Celsius Temperature
temp_Cent = int(input("Enter your Centigrade Temperature: "))

#Process
#This is the formula
temp_Fahr = temp_Cent*9/5+32

#Output
#It prints out the temperature in Fahrenheit
print("Your input in Fahrenheit is")
print(temp_Fahr)